def Task2():
    print('X\tY\tZ\t¬(X ⋁ Y ⋁ Z)\t¬X ⋀ ¬Y ⋀ ¬Z')
    for x in range(0, 2):
        for y in range(0, 2):
            for z in range(0, 2):
                result1 = not (x or y or z)
                result2 = not x and not y and not z
                print(f'{x}\t{y}\t{z}\t{result1}\t\t{result2}')


def Task3():
    x = int(input('X = '))
    while x == 0:
        x = int(input('X не должен быть равен нулю, повторите ввод: '))

    y = int(input('Y = '))
    while y == 0:
        y = int(input('Y не должен быть равен нулю, повторите ввод: '))

    if x > 0 and y > 0:
        print('I четверть')

    if x < 0 and y > 0:
        print('II четверть')

    if x < 0 and y < 0:
        print('III четверть')

    if x > 0 and y < 0:
        print('IV четверть')


